package com.example.assignment2;

public class ListItemHistory {

    int id;
    String code;

    public ListItemHistory(int id, String code){
        this.id = id;
        this.code = code;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }
}
